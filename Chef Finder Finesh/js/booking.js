function bookNow() {
    const date = document.getElementById("date").value;
    const time = document.getElementById("time").value;
    const result = document.getElementById("result");

    if (!date || !time) {
        if (result) result.innerText = "من فضلك اختر التاريخ والوقت";
        return;
    }
    
    if (result) {
        result.innerText = "✔️ تم الحجز بنجاح في " + date + " الساعة " + time;
    }
}

